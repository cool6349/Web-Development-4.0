const sum = (num1,num2) => {
    return num1+num2;
}

const substract = (num1,num2) => {
    return num1-num2;
}

const multiply = (num1,num2) => {
    return num1*num2;
}

const division = (num1,num2) => {
    return num1/num2;
}

module.exports = {
    sum,
    substract,
    multiply,
    division,
};